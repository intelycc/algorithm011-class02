class Solution {
    public boolean isValidSudoku(char[][] board) {
        if(board == null || board.length == 0 || board[0].length == 0) return true;
        Map<Integer, Set<Integer>> rowMap = new HashMap<>();
        Map<Integer, Set<Integer>> colMap = new HashMap<>();
        Map<Integer, Set<Integer>> blockMap = new HashMap<>();
        //åˆå§‹åŒ–
        for (int i = 0; i < board.length; i++) {
            rowMap.put(i, new HashSet<Integer>());
            colMap.put(i, new HashSet<Integer>());
            blockMap.put(i, new HashSet<Integer>());
        }

        for (int i = 0; i < board.length; i++) {
            for (int j = 0; j < board[i].length; j++) {
                if (board[i][j] == '.') continue;
                int num = board[i][j] - '0';
                int iPos = i / 3;
                int jPos = j / 3;
                int blockIdx = iPos * 3 + jPos;
                if (rowMap.get(i).contains(num)
                        || colMap.get(j).contains(num)
                        || blockMap.get(blockIdx).contains(num)) {
                    return false;
                }
                rowMap.get(i).add(num);
                colMap.get(j).add(num);
                blockMap.get(blockIdx).add(num);
            }
        }
        return true;
    }
}
